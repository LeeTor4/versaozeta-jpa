package txtSped;

public class CodigosDePara {

	private String codigoDe;
	private String codigoPara;
	private String codigoDeMatriz;
	
	public String getCodigoDe() {
		return codigoDe;
	}
	public void setCodigoDe(String codigoDe) {
		this.codigoDe = codigoDe;
	}
	public String getCodigoPara() {
		return codigoPara;
	}
	public void setCodigoPara(String codigoPara) {
		this.codigoPara = codigoPara;
	}
	public String getCodigoDeMatriz() {
		return codigoDeMatriz;
	}
	public void setCodigoDeMatriz(String codigoDeMatriz) {
		this.codigoDeMatriz = codigoDeMatriz;
	}
	
	
	
	
}
